    @Override
    public void timKiem() {
        String from = txtBegin.getText().trim();
        String to = txtEnd.getText().trim();
        String tenSP = txtTenSP.getText().trim();

        // ‚úÖ Ki·ªÉm tra r·ªóng

        // ‚úÖ Ki·ªÉm tra ƒë·ªãnh d·∫°ng ng√†y yyyy-MM-dd
        

        // ‚úÖ Ki·ªÉm tra t√™n s·∫£n ph·∫©m r·ªóng
        if (tenSP.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p t√™n s·∫£n ph·∫©m ƒë·ªÉ t√¨m ki·∫øm.");
            return;
        }

        // ‚úÖ Ki·ªÉm tra t√™n s·∫£n ph·∫©m h·ª£p l·ªá (ch·ªâ ch·ªØ, s·ªë, d·∫•u c√°ch, ti·∫øng Vi·ªát)
        if (!tenSP.matches("^[a-zA-Z0-9√Ä-·ªπ\\s]+$")) {
            JOptionPane.showMessageDialog(this, "T√™n s·∫£n ph·∫©m kh√¥ng h·ª£p l·ªá.");
            return;
        }

        // ‚úÖ L·∫•y d·ªØ li·ªáu t·ª´ CSDL
        List<LichSu> list = dao.searchByDateRangeAndProduct(username, from, to, tenSP);

        // ‚úÖ Ki·ªÉm tra c√≥ d·ªØ li·ªáu kh√¥ng
        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kh√¥ng t√¨m th·∫•y ƒë∆°n h√†ng ph√π h·ª£p.");
            model_.setRowCount(0);
            return;
        }

        // ‚úÖ ƒê·ªï d·ªØ li·ªáu l√™n b·∫£ng
        model_.setRowCount(0);
        for (LichSu ls : list) {
            model_.addRow(new Object[]{
                ls.getMaDH(), ls.getTenSP(), ls.getSoLuong(),
                ls.getDonGia(), ls.getTrangThai(), ls.getNgayMua()
            });
        }
    }

    @Override
    public void xoaLichSu() {
        if (model_.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Kh√¥ng c√≥ d·ªØ li·ªáu ƒë·ªÉ x√≥a.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "B·∫°n mu·ªën x√≥a to√†n b·ªô l·ªãch s·ª≠?",
                "X√°c nh·∫≠n",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            dao.deleteLichSuByUsername(username);
            loadData();
            JOptionPane.showMessageDialog(this, "ƒê√£ x√≥a to√†n b·ªô l·ªãch s·ª≠.");
        }
    }

    @Override
    public void loadData() {
        List<LichSu> list = dao.findAll();
        fillToTable(list);

    }

    @Override
    public void selecttime() {
        TimeRange range = TimeRange.today();
        switch (cboTimeRanges.getSelectedIndex()) {
            case 0 ->
                range = TimeRange.today();
            case 1 ->
                range = TimeRange.thisWeek();
            case 2 ->
                range = TimeRange.thisMonth();
            case 3 ->
                range = TimeRange.thisQuarter();
            case 4 ->
                range = TimeRange.thisYear();
        }
        txtBegin.setText(XDate.format(range.getBegin()));
        txtEnd.setText(XDate.format(range.getEnd()));

    }

    private void fillToTable(List<LichSu> list) {
        DefaultTableModel model = (DefaultTableModel) tblLichSu.getModel();
        model.setRowCount(0);
        for (LichSu ls : list) {
            model.addRow(new Object[]{
                ls.getMaDH(),
                ls.getTenSP(),
                ls.getSoLuong(),
                ls.getDonGia(),
                ls.getNgayMua(),
                ls.getTrangThai() // üëà Th√™m d√≤ng n√†y
            });
        }
    }

