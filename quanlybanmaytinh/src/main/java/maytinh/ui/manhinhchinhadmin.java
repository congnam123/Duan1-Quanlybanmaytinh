    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        // Gán giá trị vào combo khi mở form
        cboKhoangTG.removeAllItems(); // Nếu cần xóa cũ
        cboKhoangTG.addItem("Tùy chọn");
        cboKhoangTG.addItem("Hôm nay");
        cboKhoangTG.addItem("Hôm qua");
        cboKhoangTG.addItem("Tuần này");
        cboKhoangTG.addItem("Tháng này");

        cboKhoangTG.setSelectedItem("Hôm nay"); // Chọn mặc định là "Hôm nay"
        xuLyKhoangThoiGian(); // Cập nhật ngày luôn
        hienThiBieuDoBanChay();

        // Tự động hiển thị biểu đồ từ đầu tháng đến hôm nay
        LocalDate homNay = LocalDate.now();
        LocalDate dauThang = homNay.withDayOfMonth(1);

        Date tuNgay = Date.valueOf(dauThang);
        Date denNgay = Date.valueOf(homNay);

        hienThiBieuDoPieTheoNgay(tuNgay, denNgay);

        // Nếu bạn có set text cho txtTuNgay, txtDenNgay:
        txtTuNgay.setText(tuNgay.toString());
        txtDenNgay.setText(denNgay.toString());

    }//GEN-LAST:event_formWindowOpened

