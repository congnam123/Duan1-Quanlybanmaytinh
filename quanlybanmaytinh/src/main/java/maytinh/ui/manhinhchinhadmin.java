    public void locVaHienThiBieuDo() {
        try {
            String tuNgay = txtTuNgay.getText().trim();
            String denNgay = txtDenNgay.getText().trim();
            String loaiSP = cboLoai.getSelectedItem().toString();

            String sql = "SELECT sp.TenSP, SUM(ctdh.SoLuong * ctdh.DonGia) AS TongTien "
                    + "FROM ChiTietDonHang ctdh "
                    + "JOIN SanPham sp ON ctdh.MaSP = sp.MaSP "
                    + "JOIN DonHang dh ON ctdh.MaDH = dh.MaDH "
                    + "JOIN LoaiSanPham lsp ON sp.MaLoai = lsp.MaLoai "
                    + "WHERE dh.NgayLap BETWEEN ? AND ? ";

            boolean locTheoLoai = !loaiSP.equalsIgnoreCase("Tất cả");
            if (locTheoLoai) {
                sql += "AND lsp.TenLoai = ? ";
            }

            sql += "GROUP BY sp.TenSP";

            Connection conn = XJdbc.getConnection();
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, tuNgay);
            pstmt.setString(2, denNgay);

            if (locTheoLoai) {
                pstmt.setString(3, loaiSP);
            }

            ResultSet rs = pstmt.executeQuery();
            DefaultPieDataset dataset = new DefaultPieDataset();

            while (rs.next()) {
                String tenSP = rs.getString("TenSP");
                double tongTien = rs.getDouble("TongTien");
                dataset.setValue(tenSP, tongTien);
            }

            String tieuDe = locTheoLoai
                    ? "Doanh thu (" + loaiSP + ") từ " + tuNgay + " đến " + denNgay
                    : "Doanh thu từ " + tuNgay + " đến " + denNgay;

            JFreeChart pieChart = ChartFactory.createPieChart(
                    tieuDe,
                    dataset,
                    true, true, false
            );

            ChartPanel chartPanel = new ChartPanel(pieChart);
            chartPanel.setPreferredSize(new java.awt.Dimension(500, 400));

            bieudo.removeAll();
            bieudo.setLayout(new BorderLayout());
            bieudo.add(chartPanel, BorderLayout.CENTER);
            bieudo.validate();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi lọc dữ liệu: " + e.getMessage());
        }
    }

    private void xuLyKhoangThoiGian() {
        LocalDate homNay = LocalDate.now();
        DateTimeFormatter fmt = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        String chon = cboKhoangTG.getSelectedItem().toString();
        LocalDate tu, den;

        switch (chon) {
            case "Hôm nay":
                tu = den = homNay;
                break;
            case "Hôm qua":
                tu = den = homNay.minusDays(1);
                break;
            case "Tuần này":
                tu = homNay.with(DayOfWeek.MONDAY);
                den = homNay.with(DayOfWeek.SUNDAY);
                break;
            case "Tháng này":
                tu = homNay.withDayOfMonth(1);
                den = homNay.withDayOfMonth(homNay.lengthOfMonth());
                break;
            default: // "Tùy chọn"
                return; // Không tự động thay đổi
        }

        txtTuNgay.setText(fmt.format(tu));
        txtDenNgay.setText(fmt.format(den));
    }

    public void hienThiBieuDoBanChay() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        String sql = "SELECT sp.TenSP, SUM(ct.SoLuong) AS TongSoLuong "
                + "FROM ChiTietDonHang ct "
                + "JOIN SanPham sp ON ct.MaSP = sp.MaSP "
                + "GROUP BY sp.TenSP "
                + "ORDER BY TongSoLuong DESC";

        try (Connection con = XJdbc.getConnection(); PreparedStatement ps = con.prepareStatement(sql); ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                String tenSP = rs.getString("TenSP");
                int soLuong = rs.getInt("TongSoLuong");
                dataset.addValue(soLuong, "Số lượng", tenSP);
            }

            JFreeChart barChart = ChartFactory.createBarChart(
                    "Top sản phẩm bán chạy",
                    "Sản phẩm",
                    "Số lượng bán",
                    dataset
            );

            barChart.getTitle().setFont(new Font("Arial", Font.BOLD, 18));
            barChart.getLegend().setItemFont(new Font("Arial", Font.PLAIN, 14));
            barChart.setBackgroundPaint(Color.WHITE);

            CategoryPlot plot = barChart.getCategoryPlot();
            plot.getRenderer().setSeriesPaint(0, new Color(0, 153, 255));
            plot.setBackgroundPaint(Color.lightGray);
            plot.setRangeGridlinePaint(Color.white);

            ChartPanel chartPanel = new ChartPanel(barChart);
            chartPanel.setPreferredSize(new java.awt.Dimension(pnlBieuDoBanChay.getWidth(), pnlBieuDoBanChay.getHeight()));

            pnlBieuDoBanChay.removeAll();
            pnlBieuDoBanChay.setLayout(new java.awt.BorderLayout());
            pnlBieuDoBanChay.add(chartPanel, java.awt.BorderLayout.CENTER);
            pnlBieuDoBanChay.validate();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

