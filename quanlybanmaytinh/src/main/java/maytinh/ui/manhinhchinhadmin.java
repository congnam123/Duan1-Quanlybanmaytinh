    public void hienThiBieuDoPieTheoNgay(Date tuNgay, Date denNgay) {
        try {
            String sql = "SELECT sp.TenSP, SUM(ctdh.SoLuong) AS TongSoLuong, "
                    + "SUM(ctdh.SoLuong * ctdh.DonGia) AS TongTien "
                    + "FROM ChiTietDonHang ctdh "
                    + "JOIN SanPham sp ON ctdh.MaSP = sp.MaSP "
                    + "JOIN DonHang dh ON ctdh.MaDH = dh.MaDH "
                    + "WHERE dh.Ngaylap BETWEEN ? AND ? ";

            // THÊM LỌC THEO LOẠI (nếu có chọn loại cụ thể)
            String selectedLoai = cboLoai.getSelectedItem().toString();
            boolean locLoai = !selectedLoai.equalsIgnoreCase("Tất cả");
            if (locLoai) {
                sql += "AND sp.MaLoai = ? ";
            }

            sql += "GROUP BY sp.TenSP";

            // Truy vấn
            ResultSet rs;
            if (locLoai) {
                // Lấy mã loại theo tên loại (nếu cần)
                String maLoai = getMaLoaiByTen(selectedLoai); // Hàm bạn tự viết, hoặc map
                rs = XJdbc.query(sql, tuNgay, denNgay, maLoai);
            } else {
                rs = XJdbc.query(sql, tuNgay, denNgay);
            }

            DefaultPieDataset dataset = new DefaultPieDataset();
            double tongTienTatCa = 0;
            int tongSoLuongTatCa = 0;

            while (rs.next()) {
                String tenSP = rs.getString("TenSP");
                int soLuong = rs.getInt("TongSoLuong");
                double tongTien = rs.getDouble("TongTien");

                dataset.setValue(tenSP + " (" + soLuong + " sp)", tongTien);
                tongTienTatCa += tongTien;
                tongSoLuongTatCa += soLuong;
            }

            String tieuDe = String.format("Từ %s đến %s - Tổng: %, .0f VNĐ - SL: %d SP",
                    tuNgay.toString(), denNgay.toString(), tongTienTatCa, tongSoLuongTatCa);

            JFreeChart pieChart = ChartFactory.createPieChart(
                    tieuDe, dataset, true, true, false
            );

            pieChart.getTitle().setFont(new Font("Arial", Font.BOLD, 18));
            pieChart.getLegend().setItemFont(new Font("Arial", Font.PLAIN, 14));
            pieChart.setBackgroundPaint(Color.WHITE);

            ChartPanel chartPanel = new ChartPanel(pieChart);
            chartPanel.setPreferredSize(new Dimension(500, 400));

            bieudo.removeAll();
            bieudo.setLayout(new BorderLayout());
            bieudo.add(chartPanel, BorderLayout.CENTER);
            bieudo.validate();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi hiển thị biểu đồ: " + e.getMessage());
        }
    }

