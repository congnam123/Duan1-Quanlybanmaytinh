    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            List<NguoiDung> list = dao.findAll();
            for (NguoiDung nd : list) {
                model.addRow(new Object[]{
                    nd.getUsername(),
                    nd.getFullname(),
                    nd.getNgaySinh(),
                    nd.getDiaChi(),
                    nd.getSoDienThoai(),
                    nd.getPhanLoai(), // Th√™m c·ªôt ph√¢n lo·∫°i
                    nd.getTongDonHang() // Th√™m c·ªôt t·ªïng ƒë∆°n h√†ng
                });
            }
        } catch (Exception e) {
            MsgBox.alert(this, "L·ªói truy v·∫•n d·ªØ li·ªáu!");
            e.printStackTrace();
        }
    }

    @Override
    public void timKiem() {
        String keyword = txtTimKiem.getText().trim();
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);

        try {
            list = dao.findByUsername(keyword); // üîç ƒê·ªïi sang t√¨m theo Username
            for (NguoiDung nd : list) {
                int tongDon = dao.getTongDonHang(nd.getUsername());
                nd.setTongDonHang(tongDon);

                model.addRow(new Object[]{
                    nd.getUsername(),
                    nd.getFullname(),
                    nd.getNgaySinh(),
                    nd.getDiaChi(),
                    nd.getSoDienThoai(),
                    nd.getPhanLoai(),
                    nd.getTongDonHang()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "L·ªói t√¨m ki·∫øm!");
        }
    }

private void deleteNguoiDung() {
    int selectedRow = tblKhachHang.getSelectedRow();
    if (selectedRow >= 0) {
        String username = (String) tblKhachHang.getValueAt(selectedRow, 0);

        int confirm = JOptionPane.showConfirmDialog(this,
                "B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a ng∆∞·ªùi d√πng: " + username + "?",
                "X√°c nh·∫≠n x√≥a", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            dao.delete(username); // g·ªçi ƒë√∫ng ph∆∞∆°ng th·ª©c delete
            fillToTable(); // c·∫≠p nh·∫≠t l·∫°i b·∫£ng
            JOptionPane.showMessageDialog(this, "X√≥a ng∆∞·ªùi d√πng th√†nh c√¥ng!");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn ng∆∞·ªùi d√πng ƒë·ªÉ x√≥a!");
    }
}


private void updateNguoiDung() {
    int selectedRow = tblKhachHang.getSelectedRow();
    if (selectedRow >= 0) {
        try {
            String username = (String) tblKhachHang.getValueAt(selectedRow, 0);
            NguoiDung nd = dao.findOneByUsername(username); // ‚úÖ d√πng h√†m m·ªõi

            if (nd == null) {
                JOptionPane.showMessageDialog(this, "Kh√¥ng t√¨m th·∫•y ng∆∞·ªùi d√πng!");
                return;
            }

            CapNhatKhachHang dialog = new CapNhatKhachHang(null, nd);
            dialog.setLocationRelativeTo(this);
            dialog.setVisible(true);

            if (dialog.isUpdated()) {
                dao.update(dialog.getUpdatedNguoiDung());
                fillToTable();
                JOptionPane.showMessageDialog(this, "C·∫≠p nh·∫≠t th√†nh c√¥ng!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "L·ªói khi c·∫≠p nh·∫≠t!");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn ng∆∞·ªùi d√πng ƒë·ªÉ c·∫≠p nh·∫≠t!");
    }
}

