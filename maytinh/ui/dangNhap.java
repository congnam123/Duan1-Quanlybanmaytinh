    @Override
    public void login() {
    String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    // Kiểm tra rỗng
    if (username.isEmpty() || password.isEmpty()) {
        XDialog.alert("Vui lòng nhập đầy đủ tên đăng nhập và mật khẩu!");
        return;
    }

    // Gọi DAO để xác thực
    UserDAO dao = new UserDAOImpl();
    User user = dao.login(username, password);

    if (user == null) {
        XDialog.alert("Sai tên đăng nhập, mật khẩu hoặc tài khoản bị khóa!");
    } else {
        XAuth.user = (User1) user;
        XDialog.alert("Đăng nhập thành công! Xin chào " + user.getFullname());

        // Mở giao diện chính
        Giaodienchinh mainUI = new Giaodienchinh((java.awt.Frame) this.getParent(), true);
        mainUI.setVisible(true);

        // Đóng form đăng nhập
        this.dispose();
    }
        
    }

